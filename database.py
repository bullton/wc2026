import sqlite3
import os
import json
from datetime import datetime, timedelta

def init_db():
    conn = sqlite3.connect('worldcup.db')
    cursor = conn.cursor()

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS matches (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            match_date TEXT NOT NULL,
            match_time TEXT NOT NULL,
            group_name TEXT,
            home_team TEXT NOT NULL,
            away_team TEXT NOT NULL,
            home_score TEXT,
            away_score TEXT,
            home_yellow_card INTEGER DEFAULT 0,
            home_red_card INTEGER DEFAULT 0,
            away_yellow_card INTEGER DEFAULT 0,
            away_red_card INTEGER DEFAULT 0,
            venue TEXT,
            stage TEXT NOT NULL
        )
    ''')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS knockout_matrix (
            id INTEGER PRIMARY KEY,
            scenario_id INTEGER,
            qualifier_groups TEXT,
            third_places TEXT
        )
    ''')

    cursor.execute('SELECT COUNT(*) FROM matches')
    if cursor.fetchone()[0] == 0:
        insert_matches(cursor)

    cursor.execute('SELECT COUNT(*) FROM knockout_matrix')
    if cursor.fetchone()[0] == 0:
        insert_knockout_matrix(cursor)

    conn.commit()
    conn.close()

